function slugify(title) {
  return title.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/(^-|-$)/g, '')
}

function makeQuestion(lesson, index, raw) {
  const [type, prompt, choices, answer, hint, explanation, extra = {}] = raw
  const points = index < 4 ? 8 : index < 8 ? 10 : index === 8 ? 12 : 16
  const penalty = index < 4 ? 2 : index < 8 ? 3 : 4
  return {
    id: `ngss-g4-${String(lesson.episode).padStart(2, '0')}-${lesson.slug}-q${index + 1}`,
    type,
    prompt,
    choices,
    answer,
    points,
    penalty: type === 'open-response' ? 0 : penalty,
    hint,
    explanation,
    encouragement: {
      correct: index === 9 ? 'Excellent scientific thinking.' : 'Nice. You used evidence and the model.',
      incorrect: 'Good attempt. Use the hint, keep the idea in your head, and move forward.',
    },
    ...extra,
  }
}

